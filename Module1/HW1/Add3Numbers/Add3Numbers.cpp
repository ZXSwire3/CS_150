// Add3Numbers.cpp - Add two whole numbers together and print the results
// Author: Ben Foltz

#include <iostream>
using namespace std ;

int main()
{
    // Create three int variables to add using random values
    int a = 18 ;
    int b = 3 ;
    int c = 70 ;

    // Calculate their sum and print the result
    int sum = a + b + c;
    cout << "The sum of " << a << ", " << b << ", and " << c << " is " << sum << endl ;

    return 0 ;
}
